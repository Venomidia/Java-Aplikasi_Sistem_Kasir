    String iPajak, iSubTotal, iTotal;
    double [] itemcost = new double[15];
    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        /*
            Nama-nama produk
            Ayam Geprek
            Sayur Asem
            Rendang
            Ketoprak
            Gudeg
            Pempek
            Gado-Gado
            NasiUduk
            SateMadura
            Gulai
            Seblak
        */
        itemcost[0] = Double.parseDouble(txtAyamGeprek.getText()) * 1.50;
        itemcost[1] = Double.parseDouble(txtSayurAsem.getText()) * 2.50;
        itemcost[2] = Double.parseDouble(txtRendang.getText()) * 3.50;
        itemcost[3] = Double.parseDouble(txtKetoprak.getText()) * 4.50;
        itemcost[4] = Double.parseDouble(txtGudeg.getText()) * 5.50;
        itemcost[5] = Double.parseDouble(txtPempek.getText()) * 6.50; 
        itemcost[6] = Double.parseDouble(txtGado.getText()) * 7.50;
        itemcost[7] = Double.parseDouble(txtNasiUduk.getText()) * 8.50;
        itemcost[8] = Double.parseDouble(txtSateMadura.getText()) * 9.50;
        itemcost[9] = Double.parseDouble(txtGulai.getText()) * 10.50;
        itemcost[10] = Double.parseDouble(txtSeblak.getText()) * 11.50;
        
        itemcost[11] = itemcost[0] + itemcost[2] + itemcost[3] + itemcost[4] + itemcost[5] + itemcost[6];
        itemcost[12] = itemcost[7] + itemcost[8] + itemcost[9] + itemcost[10] + itemcost[11] + itemcost[12];
        
        
        iPajak = String.format("Rp. %.2f", itemcost[12]/100);
        iSubTotal = String.format("Rp. %.2f", itemcost[12]);
        iTotal = String.format("Rp. %.2f", itemcost[12] + (itemcost[12]/100));
        
        txtPajak.setText(iPajak);
        txtSubTotal.setText(iSubTotal);
        txtTotal.setText(iTotal);
        
        txtReceiptArea.setEnabled(true);
        
        int refs = 1325 + (int) (Math.random()*4238);
        
        Calendar timer = Calendar.getInstance();
        timer.getTime();
        SimpleDateFormat tTime = new SimpleDateFormat("HH:mm:ss");
        tTime.format(timer.getTime());
        SimpleDateFormat Tdate = new SimpleDateFormat("dd-MM-yyyy");
        Tdate.format(timer.getTime());
        
        txtReceiptArea.append("\n Sistem Kasir Prikitiwtralala" +
                "\n Refrensi: \t\t\t\t" + refs +
                "\n ==================================================\t" +
                "\n ==================================================\t" +
                "\nPajak:\t\t\t\t" + iPajak +
                "\nSub Total:\t\t\t\t" + iSubTotal +
                "\nTotal:\t\t\t\t" + iTotal +
                "\n ==================================================\t" +
                "\n ==================================================\t" +
                "\nDate: " + Tdate.format(timer.getTime()) + 
                "\t\tTime: " + tTime.format(timer.getTime()) +
                "\n ==================================================\t" +
                "\n ==================================================\t" +
                "\n\n\tTERIMAKASIH SUDAH BERBELANJA"+
                "\n ==================================================\t" +
                "\n ==================================================\t"
        );
        
        
    }//GEN-LAST:event_btnTotalActionPerformed

