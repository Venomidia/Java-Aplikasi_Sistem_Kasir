        //Kodingan ini digunakan hanya untuk mengetik Angka/Number pada TextField
        char iNumber = evt.getKeyChar();
        if(!(Character.isDigit(iNumber))
            ||(iNumber == KeyEvent.VK_BACKSPACE) ||(iNumber == KeyEvent.VK_DELETE))
        {
            evt.consume();
        }
